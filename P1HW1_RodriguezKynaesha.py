# Simple program for exponents, addition, and subtraction

print("-----Calculating Exponents----")
base = int(input("Enter an integer as the base value: "))
exp = int(input("Enter an integer as the exponent: "))
print(f"\n{base} raised to the power of {exp} is {base ** exp} !!")

print("\n-----Addition and Subtraction----")
a = int(input("Enter a starting integer: "))
b = int(input("Enter an integer to add: "))
c = int(input("Enter an integer to subtract: "))
print(f"\n{a} + {b} - {c} is equal to {a + b - c}")
